"""tests/test_policy_engine_benchmarks.py

Phase 6 — Policy Engine Latency Benchmarks (3 tests).

Acceptance gates:
  test_policy_evaluation_latency_p50_p95_p99
    — p50 < 1 ms, p95 < 5 ms, p99 < 10 ms for a 5-rule ruleset
      (Linux / macOS only — skipped on Windows where timing is coarser).

  test_failure_detection_rate
    — Gate A HOLD triggered >= 9/10 runs when no exec_memory_packet exists.
      HOLD trigger: Gate A evaluates exec_memory_cycle_ready=False
      (runtime never promotes exec_memory because the build_exec_memory_packet
      script is absent and the fixture has no pre-existing artifact).
      This path is active under --skip-phase-end.

  test_benchmark_regression_guard
    — Passes on first run; fails only when p99 regresses beyond 2x the
      baseline recorded by test_policy_evaluation_latency_p50_p95_p99 in
      the same pytest session.  Results are shared via a module-level dict
      to avoid filesystem race conditions in CI.

Benchmark results are written to docs/benchmarks.md once per pytest session
by a session-scoped autouse fixture.  docs/benchmarks.md is gitignored
(generated/non-canonical artifact).

All three tests carry @pytest.mark.skipif(win32) because time.perf_counter()
resolution on Windows is insufficient for sub-millisecond assertions.
"""
from __future__ import annotations

