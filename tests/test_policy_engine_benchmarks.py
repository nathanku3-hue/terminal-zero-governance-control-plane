def _make_5_rule_set() -> list[dict]:
    """Return a 5-rule policy ruleset for benchmarking.

    Rules are ordered: 4 non-matching guards, 1 matching ALLOW at the end.
    This exercises the full linear scan on every evaluate_policy call.
    """
    return [
        {
            "rule_id": f"bench-guard-{i}",
            "decision": "BLOCK",
            "scope": "gate",
            "shadow": True,
            "description": f"Benchmark guard rule {i} — does not match bench action",
            "match": {
                "field": "actor",
                "operator": "eq",
                "value": f"no-match-actor-{i}",
            },
        }
        for i in range(4)
    ] + [
        {
            "rule_id": "bench-allow-gate-a",
            "decision": "ALLOW",
            "scope": "gate",
            "shadow": False,
            "description": "Benchmark: allow gate_a PROCEED (matching rule)",
            "match": {
                "field": "actor",
                "operator": "eq",
                "value": "gate_a",
            },
        }
    ]


def _bench_action() -> dict:
    return {
        "gate": "exec_memory->advisory",
        "decision": "PROCEED",
        "trace_id": "bench-trace-001",
        "actor": "gate_a",
    }


