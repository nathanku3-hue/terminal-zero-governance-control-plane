# ---------------------------------------------------------------------------
# Module-level in-session results store
# BLOCKER 3 fix: share benchmark results within the pytest session via this
# dict rather than reading/writing docs/benchmarks.md mid-test.  This
# eliminates the execution-order race between the latency test and the
# regression guard.
# ---------------------------------------------------------------------------
_BENCHMARK_RESULTS: dict[str, Any] = {}

# Repo root — used to write docs/benchmarks.md at session end.
_REPO_ROOT = Path(__file__).resolve().parents[1]

_WIN32_SKIP = pytest.mark.skipif(
    sys.platform == "win32",
    reason="Timing benchmarks are unreliable on Windows (coarse perf_counter resolution)",
)


# ---------------------------------------------------------------------------
# Session-scoped fixture: write docs/benchmarks.md once per session
# BLOCKER 4 fix: docs/benchmarks.md is written only after all tests in the
# session have run (session scope, autouse=True).  It is gitignored so it
# never dirtys the working tree in CI.
# ---------------------------------------------------------------------------

@pytest.fixture(scope="session", autouse=True)
def _write_benchmarks_md_at_session_end():
    """Session-scoped fixture: yield (no setup), then write docs/benchmarks.md
    once with whatever _BENCHMARK_RESULTS contains at session teardown.
    """
    yield  # test session runs here

    if not _BENCHMARK_RESULTS:
        return  # nothing to write (e.g. all tests skipped on Windows)

    docs_dir = _REPO_ROOT / "docs"
    try:
        docs_dir.mkdir(parents=True, exist_ok=True)
        benchmarks_path = docs_dir / "benchmarks.md"
        lines = [
            "# Policy Engine Benchmarks",
            "",
            "*Generated by test_policy_engine_benchmarks.py — do not edit manually.*",
            "",
            "| Metric | Value |",
            "|--------|-------|",
        ]
        for key, value in sorted(_BENCHMARK_RESULTS.items()):
            if isinstance(value, float):
                lines.append(f"| {key} | {value:.4f} ms |")  
            else:
                lines.append(f"| {key} | {value} |")
        lines.append("")
        benchmarks_path.write_text("\n".join(lines), encoding="utf-8")
    except Exception:  # noqa: BLE001
        pass  # benchmark write failure must never break the test session


