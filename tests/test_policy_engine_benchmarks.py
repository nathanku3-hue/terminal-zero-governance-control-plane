# ---------------------------------------------------------------------------
# Test 1 — p50 / p95 / p99 latency for evaluate_policy
# ---------------------------------------------------------------------------

@_WIN32_SKIP
class TestPolicyEvaluationLatencyP50P95P99:
    def test_policy_evaluation_latency_p50_p95_p99(self) -> None:
        """evaluate_policy on a 5-rule ruleset must meet:
          p50 < 1 ms, p95 < 5 ms, p99 < 10 ms.

        1 000 iterations are used so that percentile estimates are stable.
        Results are stored in _BENCHMARK_RESULTS for the regression guard
        and written to docs/benchmarks.md at session teardown.
        """
        from sop._policy_engine import evaluate_policy

        rules = _make_5_rule_set()
        action = _bench_action()
        n = 1_000

        # Warm-up: discard first 50 calls (JIT / import overhead)
        for _ in range(50):
            evaluate_policy(action, rules)

        samples: list[float] = []
        for _ in range(n):
            t0 = time.perf_counter()
            evaluate_policy(action, rules)
            t1 = time.perf_counter()
            samples.append((t1 - t0) * 1_000)  # convert to milliseconds

        samples.sort()
        p50 = statistics.median(samples)
        p95 = samples[int(0.95 * n) - 1]
        p99 = samples[int(0.99 * n) - 1]

        # Store for regression guard and session-end file write
        _BENCHMARK_RESULTS["p50_ms"] = p50
        _BENCHMARK_RESULTS["p95_ms"] = p95
        _BENCHMARK_RESULTS["p99_ms"] = p99
        _BENCHMARK_RESULTS["n_samples"] = n
        _BENCHMARK_RESULTS["rule_count"] = len(rules)


        assert p50 < 1.0, (
            f"p50 latency {p50:.4f} ms exceeds 1 ms target. "
            "evaluate_policy may be performing unexpected I/O or heavy computation."
        )
        assert p95 < 5.0, (
            f"p95 latency {p95:.4f} ms exceeds 5 ms target."
        )
        assert p99 < 10.0, (
            f"p99 latency {p99:.4f} ms exceeds 10 ms target."
        )


# ---------------------------------------------------------------------------
# Test 2 -- failure detection rate (Gate A HOLD >= 9/10 runs)
# ---------------------------------------------------------------------------

@_WIN32_SKIP
class TestFailureDetectionRate:
    def test_failure_detection_rate(self, tmp_path: Path) -> None:
        hold_count = 0
        n_runs = 10
        for i in range(n_runs):
            run_tmp = tmp_path / f"run_{i}"
            run_tmp.mkdir()
            args = _minimal_hold_args(run_tmp)
            _, payload, _ = _run_cycle(args)
            final_result = payload.get("final_result", "")
            if final_result in ("HOLD", "ERROR"):
                hold_count += 1

        _BENCHMARK_RESULTS["failure_detection_rate"] = f"{hold_count}/{n_runs}"

        assert hold_count >= 9, (
            f"Failure detection rate {hold_count}/{n_runs} is below the 9/10 threshold. "
            "Gate A HOLD may not be triggering when exec memory is absent."
        )


# ---------------------------------------------------------------------------
# Test 3 -- benchmark regression guard
# ---------------------------------------------------------------------------

@_WIN32_SKIP
class TestBenchmarkRegressionGuard:
    def test_benchmark_regression_guard(self) -> None:
        if "p99_ms" not in _BENCHMARK_RESULTS:
            pytest.skip(
                "Latency benchmark results not available in this session. "
                "Run test_policy_evaluation_latency_p50_p95_p99 first."
            )

        p99_current = _BENCHMARK_RESULTS["p99_ms"]
        assert p99_current < 20.0, (
            "p99 latency exceeds 2x the 10 ms spec threshold (20 ms). "
            "This indicates a significant regression in evaluate_policy performance."
        )
